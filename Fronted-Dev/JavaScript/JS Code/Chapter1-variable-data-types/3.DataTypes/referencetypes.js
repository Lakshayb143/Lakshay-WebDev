// Reference Types.

// Any reference type is a object.

// Array

const nums = [1,2,3,4,5];

let output = nums;
console.log(output, typeof output);

// Object

const person1= {
    name : "Lakshay",
    email : "xyz@gmail.com"
};

output = person1;
console.log(output,typeof output);

