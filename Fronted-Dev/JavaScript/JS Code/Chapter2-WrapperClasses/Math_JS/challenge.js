x = Math.ceil(Math.random() * 100 );
y = Math.ceil(Math.random() * 50 )

let sum = x + y;
let subtract = x - y;
let multiply = x * y;
let divide = x / y;
let remainder = x % y;

console.log(`${x} + ${y} = ${sum} `);
console.log(`${x} - ${y} = ${subtract} `);
console.log(`${x} * ${y} = ${multiply} `);
console.log(`${x} / ${y} = ${divide} `);
console.log(`${x} % ${y} = ${remainder} `);